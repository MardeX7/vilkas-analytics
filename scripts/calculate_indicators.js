/**
 * Calculate Indicators Script
 *
 * Runs the Indicator Engine for a store and saves results to database.
 *
 * Usage:
 *   node scripts/calculate_indicators.js
 *   node scripts/calculate_indicators.js --period 7d
 *   node scripts/calculate_indicators.js --period 90d
 */

import { createClient } from '@supabase/supabase-js'
import { config } from 'dotenv'
import { calculateAllIndicators, getIndicators } from '../src/lib/indicators/engine.js'

// Load environment
config({ path: '.env.local' })

const supabaseUrl = process.env.VITE_SUPABASE_URL
const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY

if (!supabaseUrl || !serviceRoleKey) {
  console.error('‚ùå Missing VITE_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY')
  process.exit(1)
}

const supabase = createClient(supabaseUrl, serviceRoleKey)

// Default store ID (Billackering)
const STORE_ID = 'a28836f6-9487-4b67-9194-e907eaf94b69'

// Parse command line arguments
const args = process.argv.slice(2)
const periodIndex = args.indexOf('--period')
const periodLabel = periodIndex !== -1 ? args[periodIndex + 1] : '30d'

if (!['7d', '30d', '90d'].includes(periodLabel)) {
  console.error('‚ùå Invalid period. Use: 7d, 30d, or 90d')
  process.exit(1)
}

async function main() {
  console.log('‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó')
  console.log('‚ïë  üîß Indicator Engine - Calculate All Indicators              ‚ïë')
  console.log('‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù')

  const startTime = Date.now()

  try {
    // Calculate all indicators (periodEnd defaults to yesterday in engine.js)
    const results = await calculateAllIndicators({
      storeId: STORE_ID,
      supabase,
      periodLabel
      // periodEnd not specified - uses yesterday (last complete day)
    })

    // Fetch and display results
    console.log('\nüìä Fetching saved indicators...\n')

    const indicators = await getIndicators(supabase, STORE_ID, periodLabel)

    if (indicators && indicators.length > 0) {
      console.log('‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó')
      console.log('‚ïë  üìà CALCULATED INDICATORS                                    ‚ïë')
      console.log('‚ï†‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ï£')

      for (const ind of indicators) {
        const value = ind.value?.value || ind.numeric_value || '‚Äî'
        const change = ind.change_percent !== null ? `${ind.change_percent > 0 ? '+' : ''}${ind.change_percent}%` : ''
        const direction = ind.direction === 'up' ? 'üìà' : ind.direction === 'down' ? 'üìâ' : '‚ûñ'
        const alert = ind.alert_triggered ? 'üö®' : ''

        console.log(`‚ïë  ${direction} ${ind.indicator_id.padEnd(25)} ${String(value).padStart(10)} ${change.padStart(8)} ${alert}`)
      }

      console.log('‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù')
    }

    const duration = ((Date.now() - startTime) / 1000).toFixed(1)

    console.log('\n‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó')
    console.log('‚ïë  ‚úÖ COMPLETE                                                  ‚ïë')
    console.log('‚ï†‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ï£')
    console.log(`‚ïë  Success: ${String(results.success.length).padEnd(48)}‚ïë`)
    console.log(`‚ïë  Errors: ${String(results.errors.length).padEnd(49)}‚ïë`)
    console.log(`‚ïë  Skipped: ${String(results.skipped.length).padEnd(48)}‚ïë`)
    console.log(`‚ïë  Duration: ${duration}s${' '.repeat(46 - duration.length)}‚ïë`)
    console.log('‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù')

    if (results.errors.length > 0) {
      console.log('\n‚ö†Ô∏è Errors:')
      for (const err of results.errors) {
        console.log(`   - ${err.id}: ${err.error}`)
      }
    }

  } catch (err) {
    console.error('\n‚ùå FAILED:', err.message)
    process.exit(1)
  }
}

main()
